package byu.codemonkeys.tickettoride.shared.model.map;


public class City {
    private String name;

    public City(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    @Override
    public boolean equals(Object o) {
        if (o == null) {
            return false;
        }

        if (this == o) {
            return true;
        }

        if (getClass() != o.getClass()) {
            return false;
        }

        City other = (City) o;

        return this.name.equals(other.getName());
    }
}
